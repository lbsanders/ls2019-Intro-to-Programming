=begin
Write a program that outputs the factorial of the numbers 5, 6, 7, and 8.
=end

factorial_5 = 5 * (5 - 1) * (5 - 2) * (5 - 3) * (5 - 4)
factorial_6 = factorial_5 * 6
factorial_7 = factorial_6 * 7
factorial_8 = factorial_7 * 8

puts factorial_5
puts factorial_6
puts factorial_7
puts factorial_8
